package es.us.lsi.dad;

public class Placas {
	private int valueId;
	private int placasId;
	public Placas(int valueId, int placasId) {
		super();
		this.valueId = valueId;
		this.placasId = placasId;
	}
	public int getValueId() {
		return valueId; 
	}
	public int getPlacasId() {
		return placasId;
	}

	

	
}
